package com.zone24x7.rac.configservice.metadata;

/**
 * Class representing a metadata DTO.
 *
 */
public class MetadataDTO {

    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
